    private void cargarTabla(List<Entrenador> lista) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for (Entrenador e : lista) {
            model.addRow(new Object[]{
                e.getId(),
                e.getNombre(),
                e.getContacto(),
                e.getEspecialidad()
            });
        }
    }
