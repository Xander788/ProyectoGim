    private void buscarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarBtnActionPerformed
        try {
            String texto = IDpagotxt.getText().trim();
            Cliente cliente;
            cliente = controlador.buscarPorCedula(texto);
            List<Cliente> lista = new ArrayList<>();
            lista.add(cliente);
            CargarTabla(lista);

            if (lista.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No se encontraron pagos", "Info", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID debe ser num√©rico", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_buscarBtnActionPerformed

